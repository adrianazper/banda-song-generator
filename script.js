const categories = {
  Cumbias: cumbias,
  Rancheras: rancheras,
  Corridos: corridos,
  Zapateados: zapateados,
  Romanticas: romanticas
};

document.getElementById("generateBtn").addEventListener("click", function () {
  const selected = document.getElementById("myFilter").value;

  let songs;

  if (selected === "All") {
    songs = [
      ...cumbias,
      ...rancheras,
      ...corridos,
      ...zapateados,
      ...romanticas
    ];
  } else {
    songs = categories[selected];
  }

  const random = Math.floor(Math.random() * songs.length);
  const song= songs[random];

  document.getElementById("result").textContent = song.title;
  document.getElementById("lyrics").textContent = song.lyrics;
});